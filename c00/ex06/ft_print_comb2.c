#include<unistd.h>

void	print_n(int n[], int out[])
{
	if (n[0] < 10)
	{
		out[0] = 48;
		out[1] = n[0] + 48;
		if (n[1] < 10)
		{
			out[3] = 48;
			out[4] = n[1] + 48;
			return ;
		}
	}
	out[0] = n[0] / 10 + 48;
	out[1] = n[0] % 10 + 48;
	out[3] = n[1] / 10 + 48;
	out[4] = n[1] % 10 + 48;
}

void	ft_print_comb2(void)
{
	int	n[2];
	int	out[5];

	n[0] = 0;
	out[2] = 32;
	while (n[0] < 99)
	{
		n[1] = n[0] + 1;
		while (n[1] <= 99)
		{
			print_n(n, out);
			write(1, &out, 20);
			write(1, ", ", 2);
			n[1]++;
		}
		n[0]++;
	}
}
